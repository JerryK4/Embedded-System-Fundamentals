void delay_ms(uint32_t ms) {
    SysTick->LOAD = 16000 - 1;
    SysTick->VAL = 0;
    SysTick->CTRL = 5;
    for(uint32_t i = 0; i < ms; i++) {
        while((SysTick->CTRL & 0x10000) == 0);
    }
    SysTick->CTRL = 0;
}

void I2C1_Init(void) {
    RCC->AHB1ENR = 0x00000002; // GPIOBEN
    GPIOB->MODER = 0x000A0000; // PB8, PB9 Alternate
    GPIOB->AFR[1] = 0x00000044; // AF4 for PB8, PB9
    GPIOB->OTYPER = 0x00000300; // OD
    GPIOB->PUPDR = 0x00050000; // PU

    RCC->APB1ENR = 0x00200000; // I2C1EN
    I2C1->CR2 = 16;
    I2C1->CCR = 80;
    I2C1->TRISE = 17;
    I2C1->CR1 = 0x0001; // PE
}

void I2C1_Start(uint8_t address) {
    I2C1->CR1 = 0x0101; // START + PE
    while(!(I2C1->SR1 & 1)); // SB
    I2C1->DR = address;
    while(!(I2C1->SR1 & 2)); // ADDR
    (void)I2C1->SR2;
}

void I2C1_Write(uint8_t data) {
    while(!(I2C1->SR1 & 0x80));
    I2C1->DR = data;
}

void I2C1_Stop(void) {
    while(!(I2C1->SR1 & 0x84));
    I2C1->CR1 = 0x0201; // STOP + PE
}

void SSD1306_WriteCommand(uint8_t cmd) {
    I2C1_Start(SSD1306_ADDRESS);
    I2C1_Write(SSD1306_COMMAND);
    I2C1_Write(cmd);
    I2C1_Stop();
}

void SSD1306_WriteData(uint8_t data) {
    I2C1_Start(SSD1306_ADDRESS);
    I2C1_Write(SSD1306_DATA);
    I2C1_Write(data);
    I2C1_Stop();
}

void SSD1306_Init(void) {
    delay_ms(100);
    SSD1306_WriteCommand(0xAE); SSD1306_WriteCommand(0xA8);
    SSD1306_WriteCommand(0x3F); SSD1306_WriteCommand(0xD3);
    SSD1306_WriteCommand(0x00); SSD1306_WriteCommand(0x40);
    SSD1306_WriteCommand(0xA1); SSD1306_WriteCommand(0xC8);
    SSD1306_WriteCommand(0xDA); SSD1306_WriteCommand(0x12);
    SSD1306_WriteCommand(0x81); SSD1306_WriteCommand(0x7F);
    SSD1306_WriteCommand(0xA4); SSD1306_WriteCommand(0xA6);
    SSD1306_WriteCommand(0xD5); SSD1306_WriteCommand(0x80);
    SSD1306_WriteCommand(0x8D); SSD1306_WriteCommand(0x14);
    SSD1306_WriteCommand(0x20); SSD1306_WriteCommand(0x00);
    SSD1306_WriteCommand(0x21); SSD1306_WriteCommand(0); SSD1306_WriteCommand(127);
    SSD1306_WriteCommand(0x22); SSD1306_WriteCommand(0); SSD1306_WriteCommand(7);
    SSD1306_WriteCommand(0xAF);
}

void SSD1306_Clear(void) {
    for (int i = 0; i < 1024; i++) SSD1306_WriteData(0x00);
}

void SSD1306_GotoXY(uint8_t x, uint8_t y) {
    SSD1306_WriteCommand(0xB0 + y);
    SSD1306_WriteCommand(0x00 + (x & 0x0F));
    SSD1306_WriteCommand(0x10 + ((x >> 4) & 0x0F));
}

void SSD1306_PutChar(char ch) {
    if (ch < 32 || ch > 126) ch = '?';
    for (int i = 0; i < 5; i++) {
        SSD1306_WriteData(font5x7[ch - 32][i]);
    }
    SSD1306_WriteData(0x00);
}

void SSD1306_Print(char *str) {
    while (*str) SSD1306_PutChar(*str++);
}

void ADC1_Init(void) {
    RCC->AHB1ENR = 0x00000004; // GPIOCEN
    GPIOC->MODER = 0x0000000C; // PC1 analog

    RCC->APB2ENR = 0x00000100; // ADC1EN
    ADC1->SQR3 = 11;
    ADC1->SMPR1 = 0x00000038;
    ADC1->CR2 = 0x00000001; // ADON
}

uint16_t ADC1_Read(void) {
    ADC1->CR2 = 0x40000001; // SWSTART + ADON
    while (!(ADC1->SR & 2)); // EOC
    return ADC1->DR;
}

int main(void) {
    char str[20];
    I2C1_Init();
    SSD1306_Init();
    SSD1306_Clear();
    ADC1_Init();

    while (1) {
        uint16_t val = ADC1_Read();
        sprintf(str, "ADC: %4d", val);
        SSD1306_GotoXY(0, 0);
        SSD1306_Print(str);
        delay_ms(1000);
        SSD1306_Clear();
    }
}
